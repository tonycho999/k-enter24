def parse_rankings(raw_rankings_text):
    if not raw_rankings_text: return []
    parsed = []
    # ë¶ˆí•„ìš”í•œ ë§ˆí¬ë‹¤ìš´ ê¸°í˜¸ ì œê±° í›„ ì¤„ë°”ê¿ˆ ë‹¨ìœ„ë¡œ ë¶„ë¦¬
    lines = raw_rankings_text.replace('*', '').strip().split('\n')
    for i, line in enumerate(lines):
        if i >= 10: break
        try:
            line = re.sub(r'\[\d+\]', '', line).strip()
            # ìˆ«ìì™€ êµ¬ë¶„ ê¸°í˜¸ ì œê±° í›„ ì œëª©ë§Œ ì¶”ì¶œ
            title = re.sub(r'^\d+[\.\)\s-]*', '', line).strip()
            if title:
                parsed.append({
                    "rank": i + 1,
                    "title_en": title,
                    "title_kr": title,
                    "score": 100 - (i * 5)
                })
        except: continue
    return parsed

